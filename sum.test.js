const { sum } = require('./sum');

describe('sum тесты', () => {
	test('ничего не передаем', () => {
		expect(sum()).toBe(0);
	});
	test('передаем положительные числа', () => {
		expect(sum(1)(2)()).toBe(3);
	});
	test('передаем отрицательные числа', () => {
		expect(sum(-1)(-2)()).toBe(-3);
	});
	test('передаем положительные и отрицательные числа', () => {
		expect(sum(1)(2)(-1)()).toBe(2);
	});
});